namespace Spryker\Zed\NavigationGui\Communication\Form\Constraint;

use Symfony\Component\Validator\Constraint;
use Symfony\Component\Validator\ConstraintValidator;
use Symfony\Component\Validator\Exception\UnexpectedTypeException;

class UniqueKeyConstraintValidator extends ConstraintValidator
{
    /**
     * @param mixed $value
     * @param \Symfony\Component\Validator\Constraint $constraint
     *
     * @throws \Symfony\Component\Validator\Exception\UnexpectedTypeException
     *
     * @return void
     */
    public function validate($value, Constraint $constraint)
    {
        if (!$constraint instanceof UniqueKeyConstraint) {
            throw new UnexpectedTypeException($constraint, UniqueKeyConstraint::class);
        }

        $hasKey = $constraint->hasKey($value);
        if ($hasKey) {
            $this->context
                ->buildViolation($constraint->getMessage())
                ->addViolation();
        }
    }
}
